

//// Tipos de variable

//para saber el tipo typeof

var nombre = "Carlos" //String
console.log(nombre)
console.log(typeof nombre)

var number = 450 //numero
console.log(number)
console.log(typeof number)

const PI = Math.PI
console.log(PI) //decimal
console.log(typeof PI)

//numero y decimal son el mismo tipo
var verdadero = true // boolean
console.log(verdadero)
console.log(typeof verdadero)

var nulo = null //nulo
console.log(nulo)
console.log(typeof nulo)

var indefinido = undefined // indefinido
console.log(indefinido)
console.log(typeof indefinido)


