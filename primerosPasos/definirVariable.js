//// Definir Variables

//var variables globales
var saludo="Hola mundo!!"
if(saludo){
    var saludo = "Hola"
    console.log(saludo)
}
console.log(saludo)

//let, variables limitadas al alcance del bloque
let nombre = "MUNDO"
if(nombre){
    let nombre = "mnd" 
    console.log(nombre)
}
console.log(nombre)
//const, variables inmutables:::constantes
const PI = 3.1416
console.log(PI)

//fin(Definir Variables)
